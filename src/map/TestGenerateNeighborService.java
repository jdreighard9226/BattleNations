    /**
     * Tests that diagonal neighbors are correctly assigned from an even row.
     *
     * <p>Places two territories in an even row (row 0) and one territory in the
     * odd row below (row 1). Verifies that the even-row territories are assigned
     * as diagonal neighbors of the odd-row territory after {@code generateNeighbors}
     * is called.</p>
     *
     * @return true if diagonal neighbors are correctly assigned, false otherwise
     */
    private static boolean testDiagonalNeighborsAssignedFromEvenRow() {
        boolean passed = true;
        System.out.print("Testing diagonal neighbors assigned from an even row: ");

        Player player = new Player("p1", Color.RED);
        Territory t1 = new Territory(player, TerrainType.PLAIN.getTerrain(), 5, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);
        Territory t2 = new Territory(player, TerrainType.PLAIN.getTerrain(), 5, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);
        Territory t3 = new Territory(player, TerrainType.PLAIN.getTerrain(), 5, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);

        // Row 0 is even, row 1 is odd (one fewer column)
        // t3 in row 1 col 0 should be a diagonal neighbor of t1 (row 0 col 0) and t2 (row 0 col 1)
        Territory[][] grid = {
                {t1, t2},
                {t3}
        };

        GenerateNeighborService service = new GenerateNeighborService();
        service.generateNeighbors(grid);

        if (!t1.hasNeighbor(t3)) {
            passed = false;
            System.err.println("t1 should have t3 as a neighbor but does not");
        }
        if (!t3.hasNeighbor(t1)) {
            passed = false;
            System.err.println("t3 should have t1 as a neighbor but does not");
        }
        if (!t2.hasNeighbor(t3)) {
            passed = false;
            System.err.println("t2 should have t3 as a neighbor but does not");
        }
        if (!t3.hasNeighbor(t2)) {
            passed = false;
            System.err.println("t3 should have t2 as a neighbor but does not");
        }

        if (passed) {
            System.out.println("Passed");
        } else {
            System.err.println("Failed");
        }
        return passed;
    }

