    /**
     * Tests that calling generateNeighbors multiple times does not add duplicate neighbors.
     *
     * <p>Runs {@code generateNeighbors} on the same grid 100 times and verifies
     * that each territory's neighbor list does not grow beyond the expected size.</p>
     *
     * @return true if no duplicates are added across all iterations, false otherwise
     */
    private static boolean testNeighborsAreNotDuplicated() {
        boolean passed = true;
        System.out.print("Testing that neighbors are not duplicated on repeated calls: ");

        Player player = new Player("p1", Color.BLUE);
        Territory t1 = new Territory(player, TerrainType.PLAIN.getTerrain(), 5, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);
        Territory t2 = new Territory(player, TerrainType.PLAIN.getTerrain(), 5, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);

        // t1 row 0 col 0, t2 row 1 col 0 — diagonal neighbors
        Territory[][] grid = {
                {t1},
                {t2}
        };

        GenerateNeighborService service = new GenerateNeighborService();

        for (int i = 0; i < 100; i++) {
            service.generateNeighbors(grid);
        }

        int t1NeighborCount = t1.getNeighboringTerritories().size();
        int t2NeighborCount = t2.getNeighboringTerritories().size();

        if (t1NeighborCount != 1) {
            passed = false;
            System.err.println("t1 should have exactly 1 neighbor but has " + t1NeighborCount);
        }
        if (t2NeighborCount != 1) {
            passed = false;
            System.err.println("t2 should have exactly 1 neighbor but has " + t2NeighborCount);
        }

        if (passed) {
            System.out.println("Passed");
        } else {
            System.err.println("Failed");
        }
        return passed;
    }

