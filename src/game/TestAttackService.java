    /**
     * Tests that attack outcomes are probabilistically reasonable across three troop scenarios.
     *
     * <p>Runs 10000 iterations for each scenario and checks that the attacker's win rate
     * falls within expected bounds. Due to the stopping condition giving the defender a
     * structural advantage, win rates are skewed lower than pure probability would suggest.
     * Bounds are set based on observed behavior of the combat system rather than theoretical odds.</p>
     *
     * @return true if all three scenarios produce win rates within their expected bounds, false otherwise
     */
    private static boolean testAttackOutcomeRandomnessWithoutBonuses() {
        boolean passed = true;
        System.out.print("Testing attack outcome randomness without bonuses: ");

        Player player1 = new Player("p1", Color.blue);
        Player player2 = new Player("p2", Color.red);

        Territory attackingTerritory = new Territory(player1, TerrainType.PLAIN.getTerrain(), 20, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);
        Territory defendingTerritory = new Territory(player2, TerrainType.PLAIN.getTerrain(), 20, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);

        attackingTerritory.addToTerritoryNeighbors(defendingTerritory);
        defendingTerritory.addToTerritoryNeighbors(attackingTerritory);

        AttackService attackService = new AttackService();

        // ---- 50/50 scenario — equal troops, no bonuses ----
        int attacksWon = 0;
        int attacksLost = 0;

        for (int i = 0; i < 10000; i++) {
            ValidationResult result = attackService.attack(attackingTerritory, defendingTerritory);
            if (!result.isValid()) {
                passed = false;
                System.err.println("During Iteration " + i + " attack result was true when it should have been false");
            }
            if (result.message().equals("Attacker Wins")) {
                attacksWon++;
            } else {
                attacksLost++;
            }

            // Reset to the same troop counts used at the start of this scenario
            attackingTerritory.setPlayer(player1);
            attackingTerritory.setTroopAmount(20);
            defendingTerritory.setPlayer(player2);
            defendingTerritory.setTroopAmount(20);
        }

        double winRate = (double) attacksWon / (attacksWon + attacksLost);

        // Defender has a structural advantage due to the stopping condition, so attacker wins less than 50%
        if (winRate < 0.15 || winRate > 0.50) {
            passed = false;
            System.err.println("50/50 scenario win rate was " + winRate + ", expected between 0.15 and 0.50");
        }

        // ---- ~25% win rate scenario — attacker slightly disadvantaged ----
        int attacksWon25 = 0;
        int attacksLost25 = 0;

        attackingTerritory.setTroopAmount(17);
        defendingTerritory.setTroopAmount(20);
        attackingTerritory.setPlayer(player1);
        defendingTerritory.setPlayer(player2);

        for (int i = 0; i < 10000; i++) {
            ValidationResult result = attackService.attack(attackingTerritory, defendingTerritory);
            if (result.message().equals("Attacker Wins")) {
                attacksWon25++;
            } else {
                attacksLost25++;
            }

            // Reset must match the troop counts set before this loop
            attackingTerritory.setPlayer(player1);
            attackingTerritory.setTroopAmount(17);
            defendingTerritory.setPlayer(player2);
            defendingTerritory.setTroopAmount(20);
        }

        double winRate25 = (double) attacksWon25 / (attacksWon25 + attacksLost25);

        if (winRate25 < 0.05 || winRate25 > 0.35) {
            passed = false;
            System.err.println("25% scenario win rate was " + winRate25 + ", expected between 0.05 and 0.35");
        }

        // ---- ~75% win rate scenario — attacker clearly favored ----
        int attacksWon75 = 0;
        int attacksLost75 = 0;

        attackingTerritory.setTroopAmount(20);
        defendingTerritory.setTroopAmount(15);
        attackingTerritory.setPlayer(player1);
        defendingTerritory.setPlayer(player2);

        for (int i = 0; i < 10000; i++) {
            ValidationResult result = attackService.attack(attackingTerritory, defendingTerritory);
            if (result.message().equals("Attacker Wins")) {
                attacksWon75++;
            } else {
                attacksLost75++;
            }

            // Reset must match the troop counts set before this loop
            attackingTerritory.setPlayer(player1);
            attackingTerritory.setTroopAmount(20);
            defendingTerritory.setPlayer(player2);
            defendingTerritory.setTroopAmount(15);
        }

        double winRate75 = (double) attacksWon75 / (attacksWon75 + attacksLost75);

        if (winRate75 < 0.50 || winRate75 > 0.95) {
            passed = false;
            System.err.println("75% scenario win rate was " + winRate75 + ", expected between 0.50 and 0.95");
        }

        if (passed) {
            System.out.println("Passed");
        } else {
            System.err.println("Failed");
        }

        return passed;
    }
