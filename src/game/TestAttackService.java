    /**
     * Tests that a valid attack call succeeds.
     *
     * <p>Sets up two players each owning a neighboring territory with enough troops.
     * Runs 100 iterations, resetting troop counts and ownership each time to make
     * sure the result doesn't depend on leftover state from a prior iteration.</p>
     *
     * @return true if all 100 iterations return a valid result, false otherwise
     */
    private static boolean testValidAttack() {
        boolean passed = true;
        System.out.print("Testing valid attack: ");

        Player player1 = new Player("p1", Color.blue);
        Player player2 = new Player("p2", Color.red);

        Territory attackingTerritory = new Territory(player1, TerrainType.DESERT.getTerrain(), 20, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);
        Territory defendingTerritory = new Territory(player2, TerrainType.DESERT.getTerrain(), 20, new int[]{1, 2, 3, 4, 5, 6}, new int[]{1, 2, 3, 4, 5, 6}, false);

        // Make them neighbors so the attack is allowed
        attackingTerritory.addToTerritoryNeighbors(defendingTerritory);
        defendingTerritory.addToTerritoryNeighbors(attackingTerritory);

        AttackService attackService = new AttackService();

        for (int i = 0; i < 100; i++) {
            ValidationResult result = attackService.attack(attackingTerritory, defendingTerritory);
            if (!result.isValid()) {
                passed = false;
                System.err.println("During Iteration " + i + " attack result was false when it should have been true");
            }

            // Reset state between iterations so each call starts from the same conditions
            attackingTerritory.setPlayer(player1);
            attackingTerritory.setTroopAmount(20);
            defendingTerritory.setPlayer(player2);
            defendingTerritory.setTroopAmount(20);
        }

        if (passed) {
            System.out.println("Passed");
        } else {
            System.out.println("Failed");
        }

        return passed;
    }

