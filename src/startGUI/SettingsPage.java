package startGUI;

import javax.swing.*;
import java.awt.*;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

/**
 * Represents the Settings Page in the Battle Nations GUI.
 *
 * <p>This class creates a panel with options to toggle sound and music,
 * along with a button to return to the start menu. The panel uses a
 * background image and manually positions all components based on
 * screen dimensions.</p>
 *
 * <p><b>Sources Used:</b></p>
 * <ul>
 *   <li>ChatGPT – Assisted with generating Javadoc comments for this file in conjunction with Max Montowski.
 *   All comments were reviewed by the author. No code was generated by ChatGPT.</li>
 * </ul>
 *
 * @author Max Montowski
 */
public class SettingsPage {

    /**
     * Background panel displaying the settings image.
     */
    private final ImagePanel settingsPanel;

    /**
     * Checkbox for enabling/disabling sound effects.
     */
    private final JCheckBox sound;

    /**
     * Checkbox for enabling/disabling music.
     */
    private final JCheckBox music;

    /**
     * Button to return to the start menu.
     */
    private final JButton back;

    /**
     * Parent JFrame that contains this settings page.
     */
    private JFrame parent;

    /**
     * Controller for managing GUI interactions.
     */
    private StartController startController;

    /**
     * Constructs the SettingsPage and initializes all components.
     */
    public SettingsPage() {
        // Initialize background panel with image
        settingsPanel = new ImagePanel("src/gameImages/SettingsImage.png");

        // Get screen dimensions
        Dimension screen = Toolkit.getDefaultToolkit().getScreenSize();
        settingsPanel.setLayout(null);
        settingsPanel.setBounds(0, 0, screen.width, screen.height);

        // Initialize sound checkbox
        sound = new JCheckBox("Sound Affects", true);
        sound.setBounds(screen.width / 2 - 100, screen.height / 2, 200, 40);
        settingsPanel.add(sound);

        // Initialize music checkbox
        music = new JCheckBox("Music", true);
        music.setBounds(screen.width / 2 - 100, screen.height / 2 + 80, 200, 40);
        settingsPanel.add(music);

        // Initialize back button
        back = new JButton("Back To Start");
        back.setBounds(screen.width / 2 - 100, 2 * screen.height / 3, 200, 50);
        back.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Remove settings page and return to start menu
                parent.remove(settingsPanel);
                startController.displayStartMenuPage();
            }
        });
        settingsPanel.add(back);
    }

    /**
     * Adds the settings page to the specified StartController's display.
     *
     * @param startController the controller managing GUI interactions
     */
    public void addSettingsPage(StartController startController) {
        this.parent = startController.getDisplay();
        this.startController = startController;
        parent.add(settingsPanel);
        parent.repaint();
    }
}